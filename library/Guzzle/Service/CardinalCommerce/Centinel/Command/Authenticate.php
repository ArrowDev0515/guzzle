namespace Guzzle\Service\CardinalCommerce\Centinel\Command;

/**
 * The Authenticate Message is responsible for returning the Payer
 * Authentication outcome to the merchant. The message will return the status
 * of the Authentication to the merchant, en- abling the merchant to handle
 * the order/authorization processing according to the outcome.
 *
 * @author Michael Dowling <michael@shoebacca.com>
 *
 * @guzzle msg_type static="cmpi_authenticate"
 * @guzzle transaction_type required="true"
 * @guzzle order_id required="true"
 * @guzzle par_es_payload required="true"
 */
class Authenticate extends Txn
{
    /**
     * Centinel generated transaction identifier.
     *
     * @param string $value Value to set
     *
     * @return Authenticate
     */
    public function setOrderId($value)
    {
        return $this->set('order_id', $value);
    }

    /**
     * PARes generated by the external system that processed the authentication
     * with the Consumer.
     *
     * @param string $value Value to set
     *
     * @return Authenticate
     */
    public function setParEsPayload($value)
    {
        return $this->set('par_es_payload', $value);
    }
}